"""
Больше предыдущего

На вход программе подается строка текста из натуральных чисел. Из неё
формируется список чисел. Напишите программу подсчета количества чисел, которые
больше предшествующего им в этом списке числа, то есть, стоят вслед за меньшим
числом. 

Формат входных данных
На вход программе подается строка текста из разделенных пробелами натуральных
чисел.

Формат выходных данных
Программа должна вывести одно число – количество элементов списка, больших
предыдущего.
"""

numbers = list(map(int, input().split()))
print(sum(1 for i, n in enumerate(numbers[1:]) if n > numbers[i]))
# фокус в том, что i в enumerate начинается с нуля
