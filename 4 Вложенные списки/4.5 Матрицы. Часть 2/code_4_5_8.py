SIZE = 8
board = [["."] * SIZE for _ in range(SIZE)]
point = input()
i, j = "87654321".index(point[1]), "abcdefgh".index(point[0])

board[i][j] = "N"
for k in [-2, -1, 1, 2]:
    x = i + k
    if 0 <= x < SIZE:
        step = [-1, 1] if abs(k) == 2 else [-2, 2]
        for s in step:
            y = j + s
            if 0 <= y < SIZE:
                board[x][y] = "*"
[print(*row) for row in board]
