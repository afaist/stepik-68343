n = int(input())
matrix = [[int(j) for j in input().split()] for _ in range(n)]
m = int(input())
result = matrix
for i in range(m - 1):
    result = [
        [sum([result[i][p] * matrix[p][j] for p in range(n)]) for j in range(n)]
        for i in range(n)
    ]
for row in result:
    print(*row)
