n = int(input())
# –°–æ–∑–¥–∞–µ–º –º–∞—Ç—Ä–∏—Ü—É –∏ –∑–∞–ø–æ–ª–Ω—è–µ–º –ø–æ —É—Å–ª–æ–≤–∏—é
matrix = [
    [
        1 if i <= j and i <= n - j - 1 or i >= j and i >= n - j - 1 else 0
        for j in range(n)
    ]
    for i in range(n)
]
# –í—ã–≤–æ–¥ –º–∞—Ç—Ä–∏—Ü—ã
for row in matrix:
    print(*[str(x).ljust(3) for x in row])
