n, m = [int(i) for i in input().split()]
matrix = [[0] * m for _ in range(n)]
value = 1
# d - —á–∏—Å–ª–æ –¥–∏–∞–≥–æ–Ω–∞–ª–µ–π
for d in range(m + n - 1):
    for i in range(d + 1):
        # –∏–¥–µ–º –ø–æ –¥–∏–∞–≥–æ–Ω–∞–ª–∏
        if i < n and (d - i) < m:
            matrix[i][d - i] = value
            value += 1
# –≤—ã–≤–æ–¥ –º–∞—Ç—Ä–∏—Ü—ã
for row in matrix:
    print(*[str(i).ljust(2) for i in row])
