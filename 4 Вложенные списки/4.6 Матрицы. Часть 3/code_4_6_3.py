n, m = map(int, input().split())
# –≤ –∑–∞–¥–∞–Ω–∏–∏ —É–∫–∞–∑–∞–Ω–æ —Å–æ–∑–¥–∞—Ç—å –º–∞—Ç—Ä–∏—Ü—É –∏ –∑–∞–ø–æ–ª–Ω–∏—Ç—å –µ–µ
matrix = [[i * m + j for j in range(1, m + 1)] for i in range(n)]
# –≤—ã–≤–æ–¥ –º–∞—Ç—Ä–∏—Ü—ã –≤ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–∏ —Å –∑–∞–¥–∞–Ω–∏–µ–º
for i in range(n):
    for j in range(m):
        print(str(matrix[i][j]).ljust(3), end="")
    print()
